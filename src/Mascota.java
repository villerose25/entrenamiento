public class Mascota {
    // Atributos
    String raza;
    String tipo;
    String color;
    int edad;
    boolean ojosColor = false;
    boolean ladra = false;
    boolean volar = false;
    String nombre;

    public String getRaza() {
        return raza;
    }

    public void setRaza(String raza) {
        this.raza = raza;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public boolean isOjosColor() {
        return ojosColor;
    }

    public void setOjosColor(boolean ojosColor) {
        this.ojosColor = ojosColor;
    }

    public boolean isLadra() {
        return ladra;
    }

    public void setLadra(boolean ladra) {
        this.ladra = ladra;
    }

    public boolean isVolar() {
        return volar;
    }

    public void setVolar(boolean volar) {
        this.volar = volar;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }




}
